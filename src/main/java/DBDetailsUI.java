    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        DBDetails DB = new DBDetails();
        int ret = DB.fetchDetails();
        if(ret==0) {
            jTextField1.setText(DB.phost);
            jTextField2.setText(DB.pport);
            jTextField3.setText(DB.pdbname);
            jTextField4.setText(DB.puser);
            jTextField5.setText(DB.ppass);
            jTextField6.setText(DB.mhost);
            jTextField7.setText(DB.mport);
            jTextField8.setText(DB.mdbname);
            jTextField9.setText(DB.muser);
            jTextField10.setText(DB.mpass);
        }
    }//GEN-LAST:event_formWindowOpened

