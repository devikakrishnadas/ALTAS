    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DBDetails DB = new DBDetails();
        DB.phost = jTextField1.getText();
        DB.pport = jTextField2.getText();
        DB.pdbname = jTextField3.getText();
        DB.puser = jTextField4.getText();
        DB.ppass = jTextField5.getText();
        DB.mhost = jTextField6.getText();
        DB.mport = jTextField7.getText();
        DB.mdbname = jTextField8.getText();
        DB.muser = jTextField9.getText();
        DB.mpass = jTextField10.getText();
        int ret = DB.pushDetails();
        if(ret == 0 ) {
            javax.swing.JOptionPane.showMessageDialog(this,"File Saved");
        }
        else if(ret == 1){
            javax.swing.JOptionPane.showMessageDialog(this,"Config file not found");
            return;
        }
        else {
            javax.swing.JOptionPane.showMessageDialog(this,"File Not Saved");
            return;
        }
        this.setVisible(false);
        if(parent!=null) {
            parent.setVisible(true);
        }
        this.dispose();
            
    }//GEN-LAST:event_jButton2ActionPerformed

